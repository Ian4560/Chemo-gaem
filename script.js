document.addEventListener('DOMContentLoaded', () => {
    const chessBoard = document.getElementById('chess-board');
    const twoPlayerModeBtn = document.getElementById('two-player-mode');
    const singlePlayerModeBtn = document.getElementById('single-player-mode');
    const botSettings = document.getElementById('bot-settings');

    // Initialize the chessboard
    function createChessBoard() {
        for (let row = 0; row < 8; row++) {
            for (let col = 0; col < 8; col++) {
                const square = document.createElement('div');
                square.className = (row + col) % 2 === 0 ? 'white' : 'black';
                chessBoard.appendChild(square);
            }
        }
    }

    // Event listeners for modes
    twoPlayerModeBtn.addEventListener('click', () => {
        botSettings.style.display = 'none';
        alert('Two-Player Mode Selected!');
        // Add logic for two-player mode here
    });

    singlePlayerModeBtn.addEventListener('click', () => {
        botSettings.style.display = 'block';
        alert('Single-Player Mode Selected! Configure Bot Settings.');
        // Add logic for single-player mode here
    });

    // Create the board on load
    createChessBoard();
});